.set GDT_LIMIT_FIELD,                  0xFFFF
.set GDT_ACCESS_PRESENT,               0b10000000
.set GDT_ACCESS_PRIVILEGE_KERNEL,      0b00000000
.set GDT_ACCESS_PRIVILEGE_DRIVER,      0b00100000
.set GDT_ACCESS_PRIVILEGE_DRIVER_SAFE, 0b01000000
.set GDT_ACCESS_PRIVILEGE_USERMODE,    0b01100000
.set GDT_ACCESS_DESCRIPTOR_SYSTEM,     0b00010000
.set GDT_ACCESS_DESCRIPTOR_USER,       0b00000000
.set GDT_ACCESS_EXECUTABLE,            0b00001000
.set GDT_ACCESS_NON_EXECUTABLE,        0b00000000
.set GDT_ACCESS_DIRECTION_UP,          0b00000000
.set GDT_ACCESS_DIRECTION_DOWN,        0b00000100
.set GDT_ACCESS_SAME_RING,             0b00000000
.set GDT_ACCESS_LOWER_EQUAL_RING,      0b00000100
.set GDT_ACCESS_READABLE,              0b00000010
.set GDT_ACCESS_NOT_READABLE,          0b00000000
.set GDT_ACCESS_WRITEABLE,             0b00000010
.set GDT_ACCESS_NOT_WRITEABLE,         0b00000000
.set GDT_ACCESS_ACCESSED,              0b00000001
.set GDT_ACCESS_NOT_ACCESSED,          0b00000000

.set GDT_ACCESS_SYSTEM_KERNEL, GDT_ACCESS_PRIVILEGE_KERNEL | GDT_ACCESS_DESCRIPTOR_SYSTEM
.set GDT_ACCESS_USER_KERNEL, GDT_ACCESS_PRIVILEGE_KERNEL | GDT_ACCESS_DESCRIPTOR_USER
.set GDT_ACCESS_WRITE_READ, GDT_ACCESS_READABLE | GDT_ACCESS_WRITEABLE
.set GDT_ACCESS_DEFAULT, GDT_ACCESS_PRESENT | GDT_ACCESS_WRITE_READ | GDT_ACCESS_NOT_ACCESSED

.set GDT_FLAGS_GRANULARITY_BYTE, 0b00000000
.set GDT_FLAGS_GRANULARITY_PAGE, 0b10000000
.set GDT_FLAGS_SIZE_16,          0b00000000
.set GDT_FLAGS_SIZE_32,          0b01000000
.set GDT_FLAGS_LONG_64,          0b00100000
.set GDT_FLAGS_LONG_DATA,        0b00000000
.set GDT_FLAGS_LIMIT,            0b00001111

.set GDT_FLAGS_LONG_SEGMENT, GDT_FLAGS_LONG_64 | GDT_FLAGS_SIZE_16
.set GDT_FLAGS_32BIT_SEGMENT, GDT_FLAGS_SIZE_32 | GDT_FLAGS_LONG_DATA

GDT_NULL:
    .quad 0x0000000000000000

GDT_CodeKernel32:
    .word GDT_LIMIT_FIELD
    .word 0
    .byte 0
    .byte GDT_ACCESS_DEFAULT | GDT_ACCESS_SYSTEM_KERNEL | GDT_ACCESS_EXECUTABLE | GDT_ACCESS_DIRECTION_UP
    .byte GDT_FLAGS_GRANULARITY_PAGE | GDT_FLAGS_32BIT_SEGMENT | GDT_FLAGS_LIMIT
    .byte 0

GDT_DataKernel32:
    .word GDT_LIMIT_FIELD
    .word 0
    .byte 0
    .byte GDT_ACCESS_DEFAULT | GDT_ACCESS_SYSTEM_KERNEL | GDT_ACCESS_NON_EXECUTABLE | GDT_ACCESS_DIRECTION_UP
    .byte GDT_FLAGS_GRANULARITY_PAGE | GDT_FLAGS_32BIT_SEGMENT | GDT_FLAGS_LIMIT
    .byte 0

GDT_CodeUser32:
    .word GDT_LIMIT_FIELD
    .word 0
    .byte 0
    .byte GDT_ACCESS_DEFAULT | GDT_ACCESS_USER_KERNEL | GDT_ACCESS_EXECUTABLE | GDT_ACCESS_DIRECTION_UP
    .byte GDT_FLAGS_GRANULARITY_PAGE | GDT_FLAGS_32BIT_SEGMENT | GDT_FLAGS_LIMIT
    .byte 0

GDT_DataUser32:
    .word GDT_LIMIT_FIELD
    .word 0
    .byte 0
    .byte GDT_ACCESS_DEFAULT | GDT_ACCESS_USER_KERNEL | GDT_ACCESS_NON_EXECUTABLE | GDT_ACCESS_DIRECTION_UP
    .byte GDT_FLAGS_GRANULARITY_PAGE | GDT_FLAGS_32BIT_SEGMENT | GDT_FLAGS_LIMIT
    .byte 0

GDT_End:
.global GDT_Descriptor
GDT_Descriptor:
    .word GDT_End - GDT_NULL - 1   // Limit (size of GDT - 1)
    .long GDT_NULL

.global _asm_loadGDT
_asm_loadGDT:
    cmp %edi, %edi
    jz _asm_loadGDT_success
    mov $0x0, %eax
    jmp _asm_loadGDT_return
    _asm_loadGDT_success:
        lgdt (%edi)
        jmp $0x08, $flush_pipeline
        mov $0x1, %eax
    _asm_loadGDT_return:
        ret

flush_pipeline:
    mov $0x10, %eax
    mov %eax, %ds
    mov %eax, %es
    mov %eax, %fs
    mov %eax, %gs
    mov %eax, %ss
    ret

// Function to enable protected mode
.global enable_pmode
enable_pmode:
    movl %cr0, %eax
    orl $0x1, %eax
    movl %eax, %cr0
    ret
